"use client"

import * as React from "react"
import * as AlertDialogPrimitive from "@radix-ui/react-alert-dialog"

import { cn } from "@/lib/utils"
import { buttonVariants } from "@/components/ui/button"

const AlertDialog = AlertDialogPrimitive.Root

const AlertDialogTrigger = AlertDialogPrimitive.Trigger

const AlertDialogPortal = AlertDialogPrimitive.Portal

const AlertDialogOverlay = React.forwardRef<
  React.ElementRef<typeof AlertDialogPrimitive.Overlay>,
  React.ComponentPropsWithoutRef<typeof AlertDialogPrimitive.Overlay>
>(({ className, ...props }, ref) => (
  <AlertDialogPrimitive.Overlay
    className={cn(
      "fixed inset-0 z-50 bg-black/80 data-[state=open]:animate-in data-[state=closed]:animate-out data-[state=closed]:fade-out-0 data-[state=open]:fade-in-0",
      className
    )}
    {...props}
    ref={ref}
  />
))
AlertDialogOverlay.displayName = AlertDialogPrimitive.Overlay.displayName

const AlertDialogContent = React.forwardRef<
  React.ElementRef<typeof AlertDialogPrimitive.Content>,
  React.ComponentPropsWithoutRef<typeof AlertDialogPrimitive.Content>
>(({ className, ...props }, ref) => {
  const contentRef = React.useRef<HTMLDivElement>(null)
  
  // Nettoyer l'overlay et réinitialiser le focus après fermeture
  React.useEffect(() => {
    const contentElement = contentRef.current
    if (!contentElement) return

    const observer = new MutationObserver(() => {
      const state = contentElement.getAttribute('data-state')
      if (state === 'closed') {
        // Nettoyer après un court délai pour laisser l'animation se terminer
        setTimeout(() => {
          // Retirer tous les overlays et portails d'AlertDialog restants
          const portals = document.querySelectorAll('[data-radix-portal]')
          portals.forEach((portal) => {
            const overlay = portal.querySelector('[data-radix-alert-dialog-overlay]')
            const content = portal.querySelector('[role="alertdialog"]')
            if ((overlay || content) && content?.getAttribute('data-state') === 'closed') {
              portal.remove()
            }
          })
          
          // Libérer le focus de l'élément actif
          if (document.activeElement instanceof HTMLElement) {
            document.activeElement.blur()
          }
          
          // Réinitialiser le body pour permettre les interactions
          document.body.focus()
          document.body.blur()
          
          // Forcer un reflow pour réinitialiser l'état du DOM
          void document.body.offsetHeight
        }, 150)
      }
    })

    observer.observe(contentElement, {
      attributes: true,
      attributeFilter: ['data-state']
    })

    return () => {
      observer.disconnect()
    }
  }, [])

  return (
    <AlertDialogPortal>
      <AlertDialogOverlay data-radix-alert-dialog-overlay />
      <AlertDialogPrimitive.Content
        ref={(node) => {
          contentRef.current = node as HTMLDivElement
          if (typeof ref === 'function') {
            ref(node)
          } else if (ref) {
            ref.current = node
          }
        }}
        className={cn(
          "fixed left-[50%] top-[50%] z-50 grid w-full max-w-lg translate-x-[-50%] translate-y-[-50%] gap-4 border bg-background p-6 shadow-lg duration-200 data-[state=open]:animate-in data-[state=closed]:animate-out data-[state=closed]:fade-out-0 data-[state=open]:fade-in-0 data-[state=closed]:zoom-out-95 data-[state=open]:zoom-in-95 data-[state=closed]:slide-out-to-left-1/2 data-[state=closed]:slide-out-to-top-[48%] data-[state=open]:slide-in-from-left-1/2 data-[state=open]:slide-in-from-top-[48%] sm:rounded-lg",
          className
        )}
        {...props}
      />
    </AlertDialogPortal>
  )
})
AlertDialogContent.displayName = AlertDialogPrimitive.Content.displayName

